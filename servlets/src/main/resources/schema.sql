CREATE TABLE USERS (
  user_id  NUMBER AUTO_INCREMENT PRIMARY KEY,
  email    VARCHAR2(20) NOT NULL,
  password VARCHAR2(20) NOT NULL,
  f_name   VARCHAR2(50),
  i_name   VARCHAR2(50),
  o_name   VARCHAR2(50),
  dob      DATE
);

CREATE TABLE FRIENDS (
  friendship_id NUMBER AUTO_INCREMENT PRIMARY KEY,
  p_user_id     NUMBER,
  c_user_id     NUMBER
);


ALTER TABLE FRIENDS
  ADD CONSTRAINT FRIENDS_USERS_p_user_id_fk FOREIGN KEY (p_user_id) REFERENCES USERS (user_id);

ALTER TABLE FRIENDS
  ADD CONSTRAINT FRIENDS_USERS_user_id_fk FOREIGN KEY (c_user_id) REFERENCES USERS (user_id);

ALTER TABLE FRIENDS
  ADD CONSTRAINT FRIENDS_USERS_UNIQUE UNIQUE (p_user_id, c_user_id);


CREATE TABLE MESSAGES (
  correspondence_id NUMBER NOT NULL,
  user_from         NUMBER,
  user_to           NUMBER,
  date              DATETIME
);
CREATE TABLE COUNTRIES (
  country_id   NUMBER AUTO_INCREMENT PRIMARY KEY,
  country_name VARCHAR2(100)
);

CREATE TABLE AUTHORS (
  author_id  NUMBER AUTO_INCREMENT PRIMARY KEY,
  f_name     VARCHAR2(50),
  i_name     VARCHAR2(50),
  o_name     VARCHAR2(50),
  dob        DATE,
  country_id NUMBER
);
ALTER TABLE AUTHORS
  ADD CONSTRAINT AUTHORS_COUNTRIES_country_id_fk FOREIGN KEY (country_id) REFERENCES COUNTRIES (country_id);


CREATE TABLE BOOKS (
  book_id     NUMBER AUTO_INCREMENT PRIMARY KEY,
  book_name   VARCHAR2(200),
  pages_count NUMBER,
  author_id   NUMBER,
  year_name   NUMBER
);
ALTER TABLE BOOKS
  ADD CONSTRAINT BOOKS_AUTHORS_author_id_fk FOREIGN KEY (author_id) REFERENCES AUTHORS (author_id);

CREATE TABLE REVIEWS
(
  review_id NUMBER AUTO_INCREMENT PRIMARY KEY,
  user_id   NUMBER,
  book_id   NUMBER,
  review    VARCHAR2(2000)
);
ALTER TABLE REVIEWS
  ADD CONSTRAINT REVIEWS_BOOKS_book_id_fk FOREIGN KEY (book_id) REFERENCES BOOKS (book_id);
ALTER TABLE REVIEWS
  ADD CONSTRAINT REVIEWS_USERS_user_id_fk FOREIGN KEY (user_id) REFERENCES USERS (user_id);


CREATE TABLE CITIES (
  city_id   NUMBER AUTO_INCREMENT PRIMARY KEY,
  city_name VARCHAR2(100)
);

CREATE TABLE LIBRARIES (
  library_id   NUMBER AUTO_INCREMENT PRIMARY KEY,
  library_name VARCHAR2(300),
  city_id      NUMBER
);
ALTER TABLE LIBRARIES
  ADD CONSTRAINT LIBRARIES_cities_city_id_fk FOREIGN KEY (city_id) REFERENCES CITIES (city_id);

CREATE TABLE BOOK_SHOPS (
  shop_id   NUMBER AUTO_INCREMENT PRIMARY KEY,
  shop_name VARCHAR2(100),
  urls      VARCHAR2(100)
